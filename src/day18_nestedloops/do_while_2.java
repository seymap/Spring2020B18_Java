package day18_nestedloops;

public class do_while_2 {
    public static void main(String[] args) {
        int a=103;
        while (a>=100){
            System.out.println(a);
            a--;
            System.out.println("+++++++++++++++++++++++++++++++++++++++++++++=");
        }
        int a1 =104;
        do{
            System.out.println(a1);
            a1--;
        }while (a1>=100);
    }
}
